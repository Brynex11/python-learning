#Arithmetic Operators
print("Arithmetic Operators\n")
a = 4
b = 2

print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulus
print(a ** b) # Exponentiation

#relational operators
print("Relational Operators\n")
x = 10
y = 20
print(x > y)   # Greater than
print(x < y)   # Less than
print(x == y)  # Equal to
print(x != y)  # Not equal to
print(x >= y)  # Greater than or equal to
print(x <= y)  # Less than or equal to

#assignment operators
print("Assignment Operators\n")
k = 5
k += 3  # Equivalent to k = k + 3
print(k) # Output: 8

p = 10
p -= 2  # Equivalent to p = p - 2
print(p) # Output: 8

m = 4
m *= 2  # Equivalent to m = m * 2
print(m) # Output: 8

n = 16
n /= 4  # Equivalent to n = n / 4   
print(n) # Output: 4.0

v = 8
v %= 3  # Equivalent to v = v % 3
print(v) # Output: 2 

l = 2
l **= 3  # Equivalent to l = l ** 3 
print(l) # Output: 8 

#logical operators
print("Logical Operators\n")

j = True
h = False
print(j and h)  # Logical AND
print(j or h)   # Logical OR
print(not j)    # Logical NOT